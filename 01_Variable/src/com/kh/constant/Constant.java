package com.kh.constant;

public class Constant {
	/*
	 * Do you know 상수? 상수는 항상 상. 2x로 치면 2는 상수, x는 변수.
	 */
	
	public static void main(String[] args) {
		
	System.out.println("정수");
	System.out.println(7);
	System.out.println(-7);
	System.out.println("--------------------");
	
	System.out.println("실수(소수점)");
	System.out.println(7.7);
	System.out.println(-7.7);
	System.out.println("--------------------");
	
	System.out.println("(단일)문자와 문자열");
	//한 음절 단일문자는 홑따옴표 '', 그 이상 문자열은 쌍따옴표 ""
	System.out.println('돈');
	System.out.println("돈 100억");
	System.out.println("--------------------");
	
	System.out.println("논리값 true / false");
	System.out.println(true);
	System.out.println(false);
	System.out.println("--------------------");
	
	System.out.println(7+8);
	System.out.println('7'+'8');
	System.out.println("7+8");
	System.out.println("7"+"8");
	
	/* 상수 명명 규칙
	 * 1) 모든 글자를 '대문자'로 표기. 띄어쓰기는 _로 대체. (공백없음.)
	 * ex) LOGIN_OK
	
	final int START_YEAR = 1998;
	*/
	
	}
}
